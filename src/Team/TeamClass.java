package Team;

public class TeamClass
{
	public int index;
	public String name;
	public String location;
	public int ranking;
	public String stadium;
	
	public TeamClass(int index, String name, String location, int ranking, String stadium)
	{
		this.index = index;
		this.name = name;
		this.location = location;
		this.ranking = ranking;
		this.stadium = stadium;
	}
}
